package ec.edu.epn.snai.Modelo;

public class Reporte8 {
    private String cedula;
    private String nombres;
    private String apellidos;
    private String rol;
    private Integer cantidadInformesCompletos;

    public Reporte8() {
    }

    public String getCedula() {
        return cedula;
    }

    public void setCedula(String cedula) {
        this.cedula = cedula;
    }

    public String getNombres() {
        return nombres;
    }

    public void setNombres(String nombres) {
        this.nombres = nombres;
    }

    public String getApellidos() {
        return apellidos;
    }

    public void setApellidos(String apellidos) {
        this.apellidos = apellidos;
    }

    public String getRol() {
        return rol;
    }

    public void setRol(String rol) {
        this.rol = rol;
    }

    public Integer getCantidadInformesCompletos() {
        return cantidadInformesCompletos;
    }

    public void setCantidadInformesCompletos(Integer cantidadInformesCompletos) {
        this.cantidadInformesCompletos = cantidadInformesCompletos;
    }
    
    
}
